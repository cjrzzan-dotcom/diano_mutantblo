/* =========================
5) 도움말 / 상점 문구 통일
formatHelp 함수 안에 !가방 추가
그리고 상점 문구를 전부 🧪 로 통일
========================= */

function formatHelp(){
  return [
    '`!시작` - 현재 채널 던전 시작',
    '`!상태` - 상태창',
    '`!가방` - 가방',
    '`!상점` - 상점',
    '`!제작목록` - 제작식 보기',
    '`!제작 슬라임검` - 제작',
    '`!장착 1` - 인벤토리 번호 장착',
    '`!자동` - 자동사냥',
    '`!초기화` - 데이터 초기화',
  ].join('\n');
}

